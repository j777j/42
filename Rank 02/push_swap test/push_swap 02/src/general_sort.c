#include "../includes/push_swap.h"

/*
 * general_sort: 处理大量数字的通用排序算法总指挥
 *
 * 功能:
 * 这是一个协调函数，它会按顺序调用排序过程的各个阶段。
 *
 * 流程:
 * 1. 初始推送: 将 A 栈中除最大的三个数之外的所有数推到 B 栈。
 * 2. 排序剩余: 对 A 栈中剩下的三个数进行排序。
 * 3. (未来) 贪心回填: 将 B 栈的数以最优成本推回 A 栈。
 * 4. (未来) 最终对齐: 旋转 A 栈，使最小值在正确位置。
 */
void	general_sort(t_stack *a, t_stack *b)
{
	int	a_pos;
	int	b_pos;

	// 阶段一: 将 A 栈中除最大的三个数之外的所有数推到 B 栈
	push_all_except_three(a, b);
	// 阶段二: 对 A 栈中剩下的三个数进行排序
	sort_3_numbers(a);
	// 阶段三: 贪心回填主循环
	while (b->stack_size > 0)
	{
		// 找到 B 栈中移动成本最低的元素
		find_best_move(a, b, &a_pos, &b_pos);
		// 执行找到的最佳移动方案
		execute_best_move(a, b, a_pos, b_pos);
		// 将现在位于 B 栈顶的最佳元素推回 A 栈
		op_pa(a, b, true);
	}
	// 阶段四: 最终对齐
	final_alignment(a);
}
