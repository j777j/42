#include "../includes/push_swap.h"

/*
 * fill_stack_a: 将解析后的数字填充到栈 A
 *
 * 功能:
 * 1. 遍历包含已验证整数的数组。
 * 2. 将每个整数依次放入 A 栈的存储空间中。
 * 3. 更新 A 栈的 top 和 stack_size 属性。
 * 4. 释放临时的整数数组 `nums`，因为它已经被复制到栈中，不再需要。
 *
 * 参数:
 * a:     指向 A 栈的指针。
 * nums:  一个包含已验证和解析过的整数的数组。
 * count: 数组中整数的数量。
 */
void	fill_stack_a(t_stack *a, int *nums, int count)
{
	int	i;

	i = 0;
	while (i < count)
	{
		// 我们的栈实现中，栈底是索引0，新元素向后添加
		a->storage[i] = nums[i];
		a->stack_size++;
		i++;
	}
	// 循环结束后，栈顶是最后一个元素的索引
	a->top = count - 1;
	// 释放由 parse_and_validate_input 分配的临时数组，防止内存泄漏
	free(nums);
}
